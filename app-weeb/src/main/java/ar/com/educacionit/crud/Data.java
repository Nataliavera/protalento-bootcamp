package ar.com.educacionit.crud;

public class Data {

	public Data(Long id, String titulo) {
	}
	
	
	public Data() {
	}


	private Long id;
	private String titulo;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	} 
	
	
}
